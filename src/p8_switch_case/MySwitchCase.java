package p8_switch_case;

public class MySwitchCase {
    public static void main(String[] args){
        char a = 'a';
        switch (a){
            case 'a':
                System.out.println("1");
                break;
            case '2':
                System.out.println("2");
                break;
            default:
                System.out.println("default");
        }
    }
}
